export const getScholarshipsByCountry = (countryId: string): Scholarship[] => {
  return scholarships.filter(s => s.countryId === countryId);
};

export const getScholarshipById = (id: string): Scholarship | undefined => {
  return scholarships.find(s => s.id === id);
};
